// arrays : - arrays are group of values

// push pop shift unshift slice

var arr = [1,2,3,4,5,6,7]
arr.push(8)
console.log(arr)



var arr1 = [1,2,3,4,5,6,7]
arr1.pop()
console.log(arr1)


var arr2 = [1,2,3,4,5,6,7]
arr2.unshift(0)
console.log(arr2)


var arr3 = [1,2,3,4,5,6]
arr3.shift()
console.log(arr3)



var arr4 = [1,2,3,4,5,6,7]
arr4.splice(2,2)
console.log(arr4)
